/*
	Hello :)
	
	L'objet console donne accès à la console de débogage du navigateur 
	(par exemple : la Console Web dans Firefox). Les spécificités de 
	fonctionnement varient d'un navigateur à l'autre, mais il y a tout 
	de même un ensemble de fonctionnalités qui sont fournies de base.
	
	console.log()
		Permet d'afficher des messages dans la console. Vous pouvez 
		utiliser les caractères de substitution et des arguments 
		supplémentaires avec cette méthode.
	
	ex.: 
		console.log('Hello world!');		// Hello World!
		console.log('Hello %s!', 'Tony');	// Hello Tony!
		
	console.warn()
		Outputs a warning message. You may use string substitution 
		and additional arguments with this method.
		
	ex.: 
		console.warn('Hello world!');		// Hello World!
		console.warn('Hello %s!', 'Tony');	// Hello Tony!
		
	console.error()
		Outputs an error message. You may use string substitution 
		and additional arguments with this method.
	
	ex.: 
		console.error('Hello world!');		// Hello World!
		console.error('Hello %s!', 'Tony');	// Hello Tony!
	
*/